# ── _load_manifest_for_cli tests ─────────────────────────────────────────────


class TestLoadManifestForCli:
    def _write_toml(self, tmp_path: Path, content: str) -> Path:
        p = tmp_path / "leafhub.toml"
        p.write_text(textwrap.dedent(content), encoding="utf-8")
        return p

    def test_load_minimal(self, tmp_path: Path):
        from leafhub.cli import _load_manifest_for_cli

        self._write_toml(tmp_path, """
            [project]
            name = "testapp"

            [[bindings]]
            alias = "ai"
            required = true
        """)
        m = _load_manifest_for_cli(tmp_path / "leafhub.toml")
        assert m["name"] == "testapp"
        assert len(m["bindings"]) == 1
        assert m["bindings"][0]["alias"] == "ai"
        assert m["bindings"][0]["required"] is True

    def test_load_full(self, tmp_path: Path):
        from leafhub.cli import _load_manifest_for_cli

        self._write_toml(tmp_path, """
            [project]
            name = "fullapp"
            python = ">=3.12"

            [[bindings]]
            alias = "rewrite"
            required = true
            env_prefix = "REWRITE"
            capabilities = ["chat"]

            [setup]
            extra_deps = ["playwright install chromium"]
            post_register = ["python setup.py"]
            doctor_cmd = "python check.py"

            [env_fallbacks]
            rewrite = ["REWRITE_API_KEY", "OPENAI_API_KEY"]
        """)
        m = _load_manifest_for_cli(tmp_path / "leafhub.toml")
        assert m["name"] == "fullapp"
        assert m["python"] == ">=3.12"
        assert m["bindings"][0]["env_prefix"] == "REWRITE"
        assert m["setup"]["extra_deps"] == ["playwright install chromium"]
        assert m["setup"]["doctor_cmd"] == "python check.py"
        assert m["env_fallbacks"]["rewrite"] == ["REWRITE_API_KEY", "OPENAI_API_KEY"]

    def test_missing_name_raises(self, tmp_path: Path):
        from leafhub.cli import _load_manifest_for_cli

        self._write_toml(tmp_path, """
            [project]
            python = ">=3.11"
        """)
        with pytest.raises(ValueError, match="name"):
            _load_manifest_for_cli(tmp_path / "leafhub.toml")


# ── cmd_init tests ───────────────────────────────────────────────────────────


class TestCmdInit:
    def test_init_generates_scripts(self, tmp_path: Path):
        from leafhub.cli import _build_parser

        (tmp_path / "leafhub.toml").write_text(textwrap.dedent("""
            [project]
            name = "init-test"

            [[bindings]]
            alias = "llm"
            required = true
        """), encoding="utf-8")

        parser = _build_parser()
        args = parser.parse_args(["init", str(tmp_path)])
        args.func(args)

        setup_sh = tmp_path / "setup.sh"
        install_sh = tmp_path / "install.sh"
        assert setup_sh.exists()
        assert install_sh.exists()
        assert setup_sh.stat().st_mode & 0o755

        content = setup_sh.read_text(encoding="utf-8")
        assert 'PROJECT_NAME="init-test"' in content
        assert 'LEAFHUB_ALIAS="llm"' in content

    def test_init_no_manifest_fails(self, tmp_path: Path, capsys):
        from leafhub.cli import _build_parser

        parser = _build_parser()
        args = parser.parse_args(["init", str(tmp_path)])

        with pytest.raises(SystemExit):
            args.func(args)



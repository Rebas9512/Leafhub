class TestGenerateSetupSh:
    def _manifest(self, **overrides) -> dict:
        base = {
            "name": "test-project",
            "python": ">=3.11",
            "bindings": [
                {"alias": "llm", "required": True, "env_prefix": "LLM",
                 "capabilities": ["chat"]},
            ],
            "setup": {
                "extra_deps": [],
                "post_register": [],
                "doctor_cmd": None,
            },
            "env_fallbacks": {},
        }
        base.update(overrides)
        return base

    def test_basic_generation(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest())
        assert 'PROJECT_NAME="test-project"' in result
        assert 'LEAFHUB_ALIAS="llm"' in result
        assert "#!/usr/bin/env bash" in result
        assert "Generated by: leafhub init" in result

    def test_python_version_check_311(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest(python=">=3.11"))
        assert 'PY_MINOR" -lt 11' in result
        assert "Python >=3.11 required" in result

    def test_python_version_check_312(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest(python=">=3.12"))
        assert 'PY_MINOR" -lt 12' in result

    def test_python_none_defaults_to_310(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest(python=None))
        assert 'PY_MINOR" -lt 10' in result

    def test_extra_deps_step(self):
        from leafhub.scaffold import generate_setup_sh

        m = self._manifest()
        m["setup"]["extra_deps"] = ["playwright install chromium"]
        result = generate_setup_sh(m)
        assert "playwright install chromium" in result
        assert "Extra dependencies" in result
        # Step count should be 6 (5 base + 1 extra)
        assert "Step 1 / 6" in result

    def test_post_register_step(self):
        from leafhub.scaffold import generate_setup_sh

        m = self._manifest()
        m["setup"]["post_register"] = ["python -m download_models"]
        result = generate_setup_sh(m)
        assert "python -m download_models" in result
        assert "Post-registration" in result

    def test_both_extra_steps(self):
        from leafhub.scaffold import generate_setup_sh

        m = self._manifest()
        m["setup"]["extra_deps"] = ["dep1"]
        m["setup"]["post_register"] = ["cmd1"]
        result = generate_setup_sh(m)
        # 5 base + 1 extra_deps + 1 post_register = 7
        assert "Step 1 / 7" in result

    def test_no_bindings_uses_default_alias(self):
        from leafhub.scaffold import generate_setup_sh

        m = self._manifest(bindings=[])
        result = generate_setup_sh(m)
        assert 'LEAFHUB_ALIAS="default"' in result

    def test_doctor_shortcut_delegates_to_leafhub(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest())
        assert "leafhub doctor" in result

    def test_four_tier_leafhub_resolution(self):
        from leafhub.scaffold import generate_setup_sh

        result = generate_setup_sh(self._manifest())
        assert "Tier 1" in result or "system leafhub binary" in result
        assert "Tier 2" in result or "venv-installed leafhub" in result
        assert "Tier 3" in result or "distributed copy" in result
        assert "Tier 4" in result or "GitHub bootstrap" in result



# ── cmd_register manifest mode tests ─────────────────────────────────────────


class TestCmdRegisterManifestDetection:
    def test_dot_triggers_manifest_mode(self, tmp_path: Path):
        """Verify that 'register .' reads leafhub.toml."""
        from leafhub.cli import _build_parser, _load_manifest_for_cli

        (tmp_path / "leafhub.toml").write_text(textwrap.dedent("""
            [project]
            name = "manifest-reg"

            [[bindings]]
            alias = "ai"
            required = true
        """), encoding="utf-8")

        parser = _build_parser()
        args = parser.parse_args(["register", ".", "--path", str(tmp_path), "--headless"])

        # We can't actually run cmd_register without a vault,
        # but we can verify the manifest would be loaded
        raw_name = args.project_name
        assert raw_name == "."

        manifest_file = tmp_path / "leafhub.toml"
        assert manifest_file.is_file()
        m = _load_manifest_for_cli(manifest_file)
        assert m["name"] == "manifest-reg"
        assert m["bindings"][0]["alias"] == "ai"

    def test_bare_name_does_not_trigger_manifest(self, tmp_path: Path):
        """A bare name like 'myapp' should NOT trigger manifest mode
        even if a directory named 'myapp' exists with leafhub.toml."""
        from leafhub.cli import _build_parser

        # Create a directory that matches the project name
        app_dir = tmp_path / "myapp"
        app_dir.mkdir()
        (app_dir / "leafhub.toml").write_text(
            '[project]\nname = "sneaky"\n', encoding="utf-8"
        )

        parser = _build_parser()
        args = parser.parse_args(["register", "myapp", "--headless"])
        # project_name should stay as "myapp", not switch to manifest mode
        assert args.project_name == "myapp"
